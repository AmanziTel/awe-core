package org.amanzi.neo.db.internal;

import org.eclipse.ui.plugin.AbstractUIPlugin;
import org.osgi.framework.BundleContext;

/**
 * TODO Purpose of
 * <p>
 * </p>
 * 
 * @author Vladislav_Kondratenko
 * @since 1.0.0
 */
public class DatabasePlugin extends AbstractUIPlugin {
    private static DatabasePlugin plugin;

    public static final String PREFERENCE_KEY_DATABASE_LOCATION = "databaseLocation";

    public static DatabasePlugin getInstance() {
        return plugin;
    }

    /*
     * (non-Javadoc)
     * @see org.osgi.framework.BundleActivator#start(org.osgi.framework.BundleContext)
     */
    @Override
    public void start(final BundleContext bundleContext) throws Exception {
        super.start(bundleContext);
        DatabasePlugin.plugin = this;
    }

    /*
     * (non-Javadoc)
     * @see org.osgi.framework.BundleActivator#stop(org.osgi.framework.BundleContext)
     */
    @Override
    public void stop(final BundleContext bundleContext) throws Exception {
        super.stop(bundleContext);
        DatabasePlugin.plugin = null;
    }
}

package org.amanzi.awe.gpeh.console.interfaces;

/**
 * <p>
 * Interface for one input element
 * </p>
 * 
 * @author TsAr
 * @since 1.0.0
 */
public interface IStructuredElement {

    /**
     * Gets the size.
     * 
     * @return the size
     */
    public long getSize();

    /**
     * Gets the description.
     * 
     * @return the description
     */
    public String getDescription();
}